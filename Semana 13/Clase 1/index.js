/* Funciones de flecha*/
// Calcular el cuadrado de un numero
 
const cuadrado = (num) => num * num;
const cuadradoRes = cuadrado(10)
console.log(cuadradoRes)

// Calcular promedio de 3 numeros

const promedio = (a, b, c) => (a + b + c)/3;
const promRes = promedio(10,6,20)
console.log(promRes)

// Calcular el area de un triangulo

const triangulo = (base, altura) => (base*altura)/2;
const area = triangulo(10,30)
console.log(area)